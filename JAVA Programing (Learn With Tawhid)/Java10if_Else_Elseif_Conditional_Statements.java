import java.util.Scanner;
public class Java10if_Else_Elseif_Conditional_Statements
{
  public static void main (String [] args)
  {
   Scanner sc = new Scanner(System.in);
   System.out.println("Enter a number");
   int num = sc.nextInt();
   if (num > 10){
     System.out.println(num+ " 10 er boro");
   }
    else{
     System.out.println("10 Jani na");
   }
   if (num > 20){
     System.out.println(num+ " 20 er choto");
   }
   else if (num > 30){
     System.out.println(num+ " 30 er choto");
   }
   else{
     System.out.println("20 Jani na");
   }
  }
}